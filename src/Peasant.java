
public class Peasant {
private int str;
private int vit;
private int dex;
private int def;

public Peasant() {
	str = 10;
	vit = 10;
	dex = 10;
	def = 10;
}

public int getStr() {
	return str;
}
public int getVit() {
	return vit;
}
public int getDef() {
	return def;
}
public int getDex() {
	return dex;
}
}
